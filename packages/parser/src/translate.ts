function serializeCopilotConfig(source: NormalizedInstructionFile): string {
  const lines: string[] = [];

  lines.push("# Copilot configuration");
  lines.push("# Generated by DirectiveOps");
  lines.push("");
  lines.push("instructions:");

  const grouped = groupDirectivesByCategory(source.directives);

  for (const category of CATEGORY_ORDER) {
    const items = grouped.get(category);
    if (!items || items.length === 0) continue;

    lines.push(`  # ${categoryToHeading(category)}`);
    for (const directive of items) {
      const text = directiveText(directive).replace(/"/g, '\\"');
      lines.push(`  - "${text}"`);
    }
  }

  lines.push("");
  return lines.join("\n");
}
